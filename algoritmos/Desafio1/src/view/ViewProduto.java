    private void AlterarBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AlterarBtActionPerformed
        // TODO add your handling code here:
        salvarAlterar = "alterar";
        int linha = this.Tabela.getSelectedRow();
        try{
            int codigoProduto = (int) this.Tabela.getValueAt(linha, 0);
            habilitarDesabilitarCampos(true);
            modelProdutos = controllerProdutos.retornarProdutoController(codigoProduto);
            this.CodigoTxt.setText(String.valueOf(modelProdutos.getIdProduto()));
            this.NomeTxt.setText(modelProdutos.getProNome());
            this.EstoqueTxt.setText(String.valueOf(modelProdutos.getProEstoque()));
            this.ValorTxt.setText(String.valueOf(modelProdutos.getProValor()));
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado!","ATENÇÃO", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_AlterarBtActionPerformed

    private void PesquisarBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PesquisarBtActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) this.Tabela.getModel();
        final TableRowSorter<TableModel> classificador = new TableRowSorter<>(modelo);
        this.Tabela.setRowSorter(classificador);
        String texto = PesquisarTxt.getText();
        classificador.setRowFilter(RowFilter.regexFilter(texto, 1));
    }//GEN-LAST:event_PesquisarBtActionPerformed

