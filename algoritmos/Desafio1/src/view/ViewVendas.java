    private void carregarVendas() {
        DefaultTableModel modelo = (DefaultTableModel) TabelaVendas.getModel();
        listaModelVendasClientes = controllerVendasCliente.retornarListaVendasClienteController();
        listaModelProdutos = controllerProdutos.retornarListaProdutoController();   
        listaModelVendasProdutos = controllerVendasProdutos.retornarListaVendasProdutosController();
        int cont = listaModelVendasClientes.size();
        int cont2 = listaModelProdutos.size();
        int cont3 = listaModelVendasProdutos.size();
        modelo.setNumRows(0);
        for (int i = 0, j =0, k = 0; i < cont && j < cont2 && k < cont3; i++, j++, k++) {
            modelo.addRow(new Object[]{
                listaModelVendasClientes.get(i).getModelVendas().getIdVenda(),
                listaModelVendasClientes.get(i).getModelClientes().getCliNome(),
                listaModelProdutos.get(j).getProNome(),
                listaModelVendasProdutos.get(k).getVenProQuantidade(),
                listaModelVendasProdutos.get(k).getVenProValor(),
                listaModelVendasClientes.get(i).getModelVendas().getVenDataVenda(),
                
            });
        }
    }

