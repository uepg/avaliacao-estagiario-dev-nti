    private void AddBt1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBt1ActionPerformed
        // TODO add your handling code here:
        
        if (QntdTxt1.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
        } else {
            modelProdutos = controllerProdutos.retornarProdutoController(Integer.parseInt(CdProdTxt.getText()));
            DefaultTableModel modelo = (DefaultTableModel) TabelaProdutosVenda1.getModel();
            int cont = 0;
            double quantidade = 0;
            quantidade = Double.parseDouble(QntdTxt1.getText());
            if (quantidade < 10) {
                quantidade = quantidade;
            }
            if (quantidade >= 10 && quantidade < 20) {
                quantidade = quantidade - (quantidade * 0.05);
            }
            if (quantidade >= 20 && quantidade < 30) {
                quantidade = quantidade - (quantidade * 0.1);
            }
            if (quantidade >= 30) {
                quantidade = quantidade - (quantidade * 0.2);
            }
            for (int i = 0; i < cont; i++) {
                modelo.setNumRows(0);
            }

            modelo.addRow(new Object[]{
                modelProdutos.getIdProduto(),
                modelProdutos.getProNome(),
                QntdTxt1.getText(),
                modelProdutos.getProValor(),
                quantidade * modelProdutos.getProValor()
            }
            );
            somarValorTotalProdutos();
            aplicarDescontosTabela();
            habilitarDesabilitarCampos(true);

        }
    }//GEN-LAST:event_AddBt1ActionPerformed

