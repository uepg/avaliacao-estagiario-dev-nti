    private void SalvarBt1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarBt1ActionPerformed
        // TODO add your handling code here:
        listaModelVendasProdutos = new ArrayList<>();
        modelVendas.setIdCliente(Integer.parseInt(CodigoClienteTxt1.getText()));
        int codigoVenda = 0, codigoProduto = 0;
        double quantidade = 0;
        quantidade = Double.parseDouble(QntdTxt1.getText());

        try {
            modelVendas.setVenDataVenda(datas.converterDataParaDateUS(new java.util.Date(System.currentTimeMillis())));
        } catch (Exception e) {
        }
        modelVendas.setVenValorLiquido(Double.parseDouble(VlrTotalTxt1.getText()));
        modelVendas.setVenValorBruto((double) quantidade * modelProdutos.getProValor());
        modelVendas.setVenDesconto(Double.parseDouble(txt2));

        codigoVenda = controllerVendas.salvarVendasController(modelVendas);

        if (codigoVenda > 0) {
            JOptionPane.showMessageDialog(this, "Venda realizada com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Erro na venda!!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }

        int cont = TabelaProdutosVenda1.getRowCount();
        for (int i = 0; i < cont; i++) {
            codigoProduto = (int) TabelaProdutosVenda1.getValueAt(i, 0);
            modelVendasProdutos = new ModelVendasProdutos();
            modelProdutos = new ModelProdutos();
            modelVendasProdutos.setIdProduto(codigoProduto);
            modelVendasProdutos.setIdVenda(codigoVenda);
            modelVendasProdutos.setVenProValor((double) TabelaProdutosVenda1.getValueAt(i, 3));
            modelVendasProdutos.setVenProQuantidade(Integer.parseInt(TabelaProdutosVenda1.getValueAt(i, 2).toString()));
            //produto
            modelProdutos.setIdProduto(codigoProduto);
            modelProdutos.setProEstoque(controllerProdutos.retornarProdutoController(codigoProduto).getProEstoque()
                    - Integer.parseInt(TabelaProdutosVenda1.getValueAt(i, 2).toString()));
            listaModelVendasProdutos.add(modelVendasProdutos);
            listaModelProdutos.add(modelProdutos);
        }

        //salvar produtos da venda
        if (controllerVendasProdutos.salvarVendasProdutosController(listaModelVendasProdutos)) {
            //alterar estoque de produtos
            controllerProdutos.alterarEstoqueProdutoController(listaModelProdutos);
            JOptionPane.showMessageDialog(this, "Produtos da venda salvos com sucesso!", "ATENÇÃ", JOptionPane.WARNING_MESSAGE);
            carregarVendas();
            limparFormulario();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao salvar produtos da venda!!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        habilitarDesabilitarCampos(false);
    }//GEN-LAST:event_SalvarBt1ActionPerformed

