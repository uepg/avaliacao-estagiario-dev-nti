    private void aplicarDescontosTabela() {
        double quantidade = 0;
        quantidade = Double.parseDouble(QntdTxt1.getText());
        if (quantidade < 10) {
            txt = "Sem desconto";
            txt2 = "0";
        }
        if (quantidade >= 10 && quantidade < 20) {
            txt = "5%";
            txt2 = "5";
        }
        if (quantidade >= 20 && quantidade < 30) {
            txt = "10%";
            txt2 = "10";
        }
        if (quantidade >= 30) {
            txt = "20%";
            txt2 = "20";
        }
        DescontoTxt1.setText(String.valueOf(txt));
    }

    private void somarValorTotalProdutos() {
        double soma = 0, valor;

        int cont = TabelaProdutosVenda1.getRowCount();
        for (int i = 0; i < cont; i++) {
            valor = (double) TabelaProdutosVenda1.getValueAt(i, 4);
            soma += valor;
        }
        VlrTotalTxt1.setText(String.valueOf(soma));
    }

