    private void habilitarDesabilitarCampos(boolean condicao){
        NomeTxt.setEnabled(condicao);
        EstoqueTxt.setEnabled(condicao);
        ValorTxt.setEnabled(condicao);
        SalvarBt.setEnabled(condicao);
        
    }
    
    private void limparCampo(){
        NomeTxt.setText("");
        EstoqueTxt.setText("");
        ValorTxt.setText("");
    }
    
    //Preenche tabela com os produtos do banco de dados.
    private void carregarProdutos(){
        listaModelProduto = controllerProdutos.retornarListaProdutoController();
        DefaultTableModel modelo = (DefaultTableModel) Tabela.getModel();
        modelo.setNumRows(0);
        //inserir produtos na tabela
        int cont = listaModelProduto.size();
        for(int i = 0; i < cont; i++){
            modelo.addRow(new Object[]{
                listaModelProduto.get(i).getIdProduto(),
                listaModelProduto.get(i).getProNome(),
                listaModelProduto.get(i).getProEstoque(),
                listaModelProduto.get(i).getProValor()
                });
        }
    }
    
    private void salvarProduto(){
        modelProdutos.setProNome(this.NomeTxt.getText());
        modelProdutos.setProEstoque(Integer.parseInt(this.EstoqueTxt.getText()));
        modelProdutos.setProValor(formatador.converterVirgulaParaPonto(this.ValorTxt.getText()));
        if(controllerProdutos.salvarProdutoController(modelProdutos)>0){
            JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso!","ATENÇÃO", JOptionPane.WARNING_MESSAGE);
            this.carregarProdutos();
            this.limparCampo();
            this.habilitarDesabilitarCampos(false);
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar produto!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void alterarProduto(){
        modelProdutos.setProNome(this.NomeTxt.getText());
        modelProdutos.setProEstoque(Integer.parseInt(this.EstoqueTxt.getText()));
        modelProdutos.setProValor(formatador.converterVirgulaParaPonto(this.ValorTxt.getText()));
        if(controllerProdutos.alterarProdutoController(modelProdutos)){
            JOptionPane.showMessageDialog(this, "Produto alterado com sucesso!","ATENÇÃO", JOptionPane.WARNING_MESSAGE);
            this.carregarProdutos();
            this.limparCampo();
            this.habilitarDesabilitarCampos(false);
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao alterar produto!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }

